// ---- i18n Core ----
function getLang() {
    return localStorage.getItem('pentexa_lang') || 'en';
}

function setLang(lang) {
    localStorage.setItem('pentexa_lang', lang);
    document.documentElement.lang = lang;
    applyTranslations();
    // Re-render navbar and footer with new language
    const oldNav = document.querySelector('.navbar');
    const oldFooter = document.querySelector('.footer');
    if (oldNav) oldNav.remove();
    if (oldFooter) oldFooter.remove();
    renderNavbar();
    renderFooter();
    initScrollEffects();
    initDropdownClose();
    if (window.reRenderProducts) {
        window.reRenderProducts();
    }
}

function t(key, fallback) {
    const lang = getLang();
    const val = TRANSLATIONS[lang]?.[key] || TRANSLATIONS['en']?.[key];
    return val !== undefined ? val : (fallback !== undefined ? fallback : key);
}

function applyTranslations() {
    document.querySelectorAll('[data-i18n]').forEach(el => {
        const key = el.getAttribute('data-i18n');
        const translation = t(key);
        if (el.hasAttribute('data-i18n-html')) {
            el.innerHTML = translation;
        } else {
            el.textContent = translation;
        }
    });
    document.querySelectorAll('[data-i18n-placeholder]').forEach(el => {
        el.placeholder = t(el.getAttribute('data-i18n-placeholder'));
    });
    document.querySelectorAll('[data-i18n-aria]').forEach(el => {
        el.setAttribute('aria-label', t(el.getAttribute('data-i18n-aria')));
    });

    // Update lang toggle button
    const btn = document.getElementById('langToggleBtn');
    if (btn) {
        btn.textContent = getLang() === 'en' ? 'TR' : 'EN';
        btn.title = getLang() === 'en' ? 'Türkçeye geç' : 'Switch to English';
    }
}

// Init on load
document.addEventListener('DOMContentLoaded', () => {
    document.documentElement.lang = getLang();
    setTimeout(applyTranslations, 50);
});
